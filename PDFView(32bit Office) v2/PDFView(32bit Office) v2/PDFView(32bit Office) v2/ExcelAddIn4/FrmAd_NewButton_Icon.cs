        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void FrmAd_NewButton_Icon_Resize(object sender, EventArgs e)
        {
            if (this.WindowState == FormWindowState.Maximized)
