        public FrmAd_NewButton_Icon()
        {
            InitializeComponent();
            rbLarge.Checked = true;
            rbSmall.Checked = false;
            size = 32;
            BindIcon(strlist);
        }
        /// <summary>
        /// 
        /// </summary>
        /// <param name="iconnames"></param>
        private void BindIcon(List<string> iconnames)
        {
            Button btn;
            int y = 0;
            int x = 0;
            panel4.Controls.Clear();
            for (int i = 0; i < iconnames.Count; i++)
            {
                try
                {
                    Image img = OleCreateConverter.PictureDispToImage(Globals.ThisAddIn.Application.CommandBars.GetImageMso(iconnames[i], size, size));
                    btn = new Button();
                    btn.Image = img;
                    btn.Name = "Pict_" + iconnames[i];
                    btn.Size = new System.Drawing.Size(64, 64);
                    btn.Location = new System.Drawing.Point(64 * (x), 64 * y);
                    btn.Click += new EventHandler(btn_Click);
                    panel4.Controls.Add(btn);
                    x++;
                    if (64 * (x + 2) > this.Width)
                    {
                        x = 0;
                        y++;
                    }
                }
                catch
                {
                    continue;
                }
            }
            ((Button)panel4.Controls[0]).Select();
        }
        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void btn_Click(object sender, EventArgs e)
        {
            frmAd_NewButton.iconName = ((Button)sender).Name.Replace("Pict_", "");
            frmAd_NewButton.size = rbLarge.Checked ? 32 : 16;
            this.Close();
        }
        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void rbSmall_CheckedChanged(object sender, EventArgs e)
        {
            if (rbSmall.Checked)
            {
                size = 16;
                chkMore_CheckedChanged(null, null);
            }
            else
            {
                size = 32;
                chkMore_CheckedChanged(null, null);
            }
        }
        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void chkMore_CheckedChanged(object sender, EventArgs e)
        {
            if (chkMore.Checked)
                BindIcon(str2list);
            else
                BindIcon(strlist);
        }
        /// <summary>
        /// 
        /// </summary>
        /// <param name="sender"></param>
